<h1>le corbeau et vincent</h1>


<p>Maitre Corbeau, sur un arbre perché
  Tenait en son bec un fromage.
  Maitre Renard, par l odeur alléché,
  Lui tint à peu pres ce langage :
  Et bonjour monsieur du corbeau ,
  Que vous etes jolie ! que vous me semblez beau !
</p>



 <p> Sans mentir, si votre ramage
           Se rapporte à votre plumage,
     Vous êtes le Phénix des hôtes de ces bois.
À ces mots le Corbeau ne se sent pas de joie,
           Et pour montrer sa belle voix,
   Il ouvre un large bec, laisse tomber sa proie.</p>

   <p>Le Renard s'en saisit, et dit : Mon bon Monsieur,
                  Apprenez que tout flatteur
         Vit aux dépens de celui qui l'écoute.
       Cette leçon vaut bien un fromage sans doute.
               Le Corbeau honteux et confus
       Jura, mais un peu tard, qu'on ne l'y prendrait plus.</p>
